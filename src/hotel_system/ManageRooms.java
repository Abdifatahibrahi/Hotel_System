    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
      DefaultTableModel model = (DefaultTableModel) jTable1.getModel(); 
      int selecetedRow = jTable1.getSelectedRow();
      
      txtNumber.setText(model.getValueAt(selecetedRow,0).toString());
      jComboBoxRoomType.setSelectedItem(model.getValueAt(selecetedRow,1));
      txtphone.setText(model.getValueAt(selecetedRow,2).toString());
      
      String isReserverd = model.getValueAt(selecetedRow,3).toString();
      
      if(isReserverd.equals("Yes")){
          btnYes.setSelected(true);
      }
      else if(isReserverd.equals("No")){
         btnNo.setSelected(true); 
      }
      
      
    }//GEN-LAST:event_jTable1MouseClicked

