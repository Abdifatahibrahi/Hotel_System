    private void btnEditRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditRoomActionPerformed
        int roomNumber = 0;
        int roomType = Integer.valueOf(jComboBoxRoomType.getSelectedItem().toString());
        String phone = txtphone.getText();
        String isReserved = "No";
        if(btnYes.isSelected()){
            isReserved = "Yes";
        }
        if(phone.trim().equals("") ){  
            JOptionPane.showMessageDialog(rootPane,"Enter the room number","empty field",JOptionPane.ERROR_MESSAGE);
            
        }
        else{
            try{
               roomNumber = Integer.valueOf(txtNumber.getText());
                if(rooms.editRoom(roomNumber,roomType,isReserved,phone)){
                JOptionPane.showMessageDialog(rootPane,"Room updated","update Room",JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(rootPane,"Room not updated","update Room error",JOptionPane.ERROR_MESSAGE);
  
            }
            }
            catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(rootPane, ex.getMessage() + "Enter the Room Number","Room error",JOptionPane.ERROR_MESSAGE);
            }
                   }
        
        
    }//GEN-LAST:event_btnEditRoomActionPerformed

    private void btnRemoveRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveRoomActionPerformed
        try{
              int id = Integer.valueOf(txtNumber.getText());
                if(rooms.deleteRoom(id)){
                JOptionPane.showMessageDialog(rootPane,"Room data deleted succesfully","delete Room",JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(rootPane,"Room not deleted","delete Room error",JOptionPane.ERROR_MESSAGE);
  
            }
            }
            catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(rootPane, ex.getMessage() + "Enter the Room Number","Room error",JOptionPane.ERROR_MESSAGE);
            }

    }//GEN-LAST:event_btnRemoveRoomActionPerformed

